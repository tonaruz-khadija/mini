#include "minishell.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t len;
	size_t i;

	if (!s)
		return ;
	i = 0;
	len = ft_strlen(s);
	while (i < len)
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}